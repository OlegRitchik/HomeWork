package HomeWork1;

public class Math1 {
    public static void main(String[] args) {
        // Получаем модуль числа.
        System.out.println(Math.abs(-5));      // 5
        System.out.println(Math.abs(-87.8d));  // 87.8
        System.out.println(Math.abs(7821L));   // 7821
        System.out.println(Math.abs(2.672f));  // 2.672

    }
}



